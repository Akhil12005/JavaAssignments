package Assignment7;
public class Prblm1 {
    public static void main(String[] args) {
        // Printing the 4th table using a for loop
        System.out.println("4th Table:");

        for (int i = 1; i <= 10; i++) {
            int result = 4 * i;
            System.out.println("4 * " + i + " = " + result);
        }
    }
}
